#Program for Area and Perimeter of rectangle
l = float(input("Enter Length:"))
b = float(input("Enter Breadth:"))
ar = l*b
pr = 2*(l+b)
print("="*50)
print("Length:{}".format(l))
print("Breadth:{}".format(b))
print("Area of Rectangle:{}".format(ar)) #Area of rectangle = (Length*Breadth)
print("Perimeter of Rectangle:{}".format(pr)) #Perimeter of rectangle = 2(l+b)
print("="*50)
